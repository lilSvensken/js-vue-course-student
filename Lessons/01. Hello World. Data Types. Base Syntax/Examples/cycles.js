// Цикл for
for (let i = 10; i < 42; i += 1) {
    console.log('i = ' + i);
    if (i === 15) continue;
    if (i === 20) break;
}




// for of - на следующем занятии
// for in - на следующем занятии





// Цикл while
let i = 30;
while (i < 42) {
    console.log('i = ' + i);
    i = i + 1;
}





// Цикл do while
let j = 30;
do {
    console.log('j = ' + j);
    j = j + 1;
} while (j < 42);